class SplashActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            PharmaXTheme {
                SplashScreen {
                    startActivity(Intent(this, LoginActivity::class.java))
                    finish()
                }
            }
        }
    }
}

@Composable
fun SplashScreen(onTimeout: () -> Unit) {
    LaunchedEffect(Unit) {
        delay(2000)
        onTimeout()
    }

    Box(
        modifier = Modifier.fillMaxSize(),
        contentAlignment = Alignment.Center
    ) {
        Column(horizontalAlignment = Alignment.CenterHorizontally) {
            // Using a placeholder for logo as specified by user "the provide is logo"
            // If R.drawable.logo doesn't exist, this might fail, so I'll use a placeholder icon if possible
            // or just text for now to be safe, but user mentioned logo.
            // I'll use ic_launcher_foreground as a placeholder.
            Image(
                painter = painterResource(id = R.drawable.unnamed1),
                contentDescription = "PharmaX Logo",
                modifier = Modifier.size(150.dp)
            )

        }
    }
}
